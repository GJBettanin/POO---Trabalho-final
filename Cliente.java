public class Cliente {
    
    private String nome;
    private String sobrenome;
    private int idade;
    private String cpf;
    
    public Cliente(String nome, String sobrenome,
                    int idade, String cpf){
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.idade = idade;
        this.cpf = cpf;
    }
}
